export default function FAQ(){
  const [openKey, setOpenKey] = useState(null) // key format: `${catIdx}-${itemIdx}`

  const toggle = (catIdx, itemIdx) => {
    const key = `${catIdx}-${itemIdx}`
    setOpenKey(prev => prev === key ? null : key)
  }

  return (
    <section id="faq" className="faq">
      <div className="container">
        <h2 className="section-title">שאלות נפוצות</h2>

        {faqData.map((cat, catIdx) => (
          <div className="faq-category" key={catIdx}>
            <div className="faq-category-header">
              <span className="faq-category-icon">{cat.icon}</span>
              <h3>{cat.label}</h3>
            </div>

            {cat.items.map((it, i) => {
              const key = `${catIdx}-${i}`
              const isOpen = openKey === key
              return (
                <div className="faq-item" key={key}>
                  <div className={`faq-question ${isOpen? 'active':''}`} onClick={()=>toggle(catIdx, i)}>
                    <span dangerouslySetInnerHTML={{__html: it.q}} />
                    <span className={`faq-icon ${isOpen? 'active':''}`}>+</span>
                  </div>
                  <div className={`faq-answer ${isOpen? 'active':''}`} dangerouslySetInnerHTML={{__html: it.a}} />
                </div>
              )
            })}
          </div>
        ))}

      </div>
    </section>
  )
}
